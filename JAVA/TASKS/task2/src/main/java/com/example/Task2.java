package com.example;

public class Task2 {
   public static void main(String[] args) {
        //Primitive Data types
        //numeric
        //for integral we have
        //integer
    
        byte age = 127;
        System.out.println(age);
        short money = 25000;
        System.out.println(money);
        //int
        long lotsofmoney = 2569364688977L;
        System.out.println(lotsofmoney);
    
        // for floating point we have
        double largearea = 123456.9;
        System.out.println(largearea);
        float area = 123456.7f;
        System.out.println(area);
    
    
        //For Character numberic data type we have to put it in a single quote
    
        char options = 'a';
        System.out.println(options);
    
        //For Boolean is a tur or false statement
    
        Boolean Isaround = false;
        System.out.println(Isaround);
    
   } 
    
}   

